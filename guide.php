			Umpire
		</h3>
		<p>
			Every game has an umpire. The role of the umpire is to run the game; moving units, calculating casualties and encounters. As result they are the only indivual who can see the whole picture.
		</p>

		<h3>
			Players
		</h3>
		<p>
			In a game, there are two teams. In a small game a teams can be made up of one person; however in larger games, teams can be compromised of multiple iniduals. In games where there are multiple indivuals, units are divided up amongst all players.
		</p>
		<p>
			Teams are only able to see there own units and enemy units that are in visual range of the a freindly unit. As a result creating a cloud of war mechanic. Teams or players sends orders to there units by hand written letters/notes. These notes/letters would be given to the umpire to be excuted.
